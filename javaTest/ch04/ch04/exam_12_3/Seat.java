package ch04.exam_12_3;

public class Seat
{
	private String name;
	private String tel;
	
	
	public Seat()
	{
		super();
	}
	
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getTel()
	{
		return tel;
	}
	public void setTel(String tel)
	{
		this.tel = tel;
	}
	
}
